// INLINE SIGNATURE PAD (Canvas-Zeichenfeld)
// ═══════════════════════════════════════════════════════════════════════════════

function InlineSignaturePad({ onSave }: { onSave: (signatureBase64: string) => void }) {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const [isDrawing, setIsDrawing] = useState(false);
  const [hasSignature, setHasSignature] = useState(false);
  const [paths, setPaths] = useState<{ x: number; y: number }[][]>([]);
  const [currentPath, setCurrentPath] = useState<{ x: number; y: number }[]>([]);
  const width = 480;
  const height = 180;

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;
    const dpr = window.devicePixelRatio || 1;
    canvas.width = width * dpr;
    canvas.height = height * dpr;
    canvas.style.width = `${width}px`;
    canvas.style.height = `${height}px`;
    ctx.scale(dpr, dpr);
    drawBackground(ctx);
  }, []);

  const drawBackground = (ctx: CanvasRenderingContext2D) => {
    ctx.fillStyle = "#ffffff";
    ctx.fillRect(0, 0, width, height);
    ctx.strokeStyle = "#e2e8f0";
    ctx.lineWidth = 1;
    ctx.beginPath();
    ctx.moveTo(20, height - 30);
    ctx.lineTo(width - 20, height - 30);
    ctx.stroke();
    ctx.strokeStyle = "#1e293b";
    ctx.lineWidth = 2;
  };

  const redrawCanvas = useCallback(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;
    drawBackground(ctx);
    ctx.strokeStyle = "#1e293b";
    ctx.lineWidth = 2;
    ctx.lineCap = "round";
    ctx.lineJoin = "round";
    paths.forEach(p => {
      if (p.length < 2) return;
      ctx.beginPath();
      ctx.moveTo(p[0].x, p[0].y);
      for (let i = 1; i < p.length; i++) ctx.lineTo(p[i].x, p[i].y);
      ctx.stroke();
    });
  }, [paths]);

  useEffect(() => { redrawCanvas(); }, [redrawCanvas]);

  const getCoords = (e: React.MouseEvent | React.TouchEvent) => {
    const canvas = canvasRef.current;
    if (!canvas) return { x: 0, y: 0 };
    const rect = canvas.getBoundingClientRect();
    if ("touches" in e) {
      const t = e.touches[0];
      return { x: t.clientX - rect.left, y: t.clientY - rect.top };
    }
    return { x: e.clientX - rect.left, y: e.clientY - rect.top };
  };

  const startDrawing = (e: React.MouseEvent | React.TouchEvent) => {
    e.preventDefault();
    setIsDrawing(true);
    const coords = getCoords(e);
    setCurrentPath([coords]);
    const ctx = canvasRef.current?.getContext("2d");
    if (ctx) { ctx.beginPath(); ctx.moveTo(coords.x, coords.y); }
  };

  const draw = (e: React.MouseEvent | React.TouchEvent) => {
    if (!isDrawing) return;
    e.preventDefault();
    const coords = getCoords(e);
    setCurrentPath(prev => [...prev, coords]);
    const ctx = canvasRef.current?.getContext("2d");
    if (ctx) {
      ctx.strokeStyle = "#1e293b";
      ctx.lineWidth = 2;
      ctx.lineCap = "round";
      ctx.lineJoin = "round";
      ctx.lineTo(coords.x, coords.y);
      ctx.stroke();
      ctx.beginPath();
      ctx.moveTo(coords.x, coords.y);
    }
    setHasSignature(true);
  };

  const stopDrawing = () => {
    if (!isDrawing) return;
    setIsDrawing(false);
    if (currentPath.length > 1) setPaths(prev => [...prev, currentPath]);
    setCurrentPath([]);
  };

  const handleClear = () => {
    setPaths([]);
    setCurrentPath([]);
    setHasSignature(false);
  };

  const handleUndo = () => {
    if (paths.length === 0) return;
    setPaths(prev => prev.slice(0, -1));
    if (paths.length === 1) setHasSignature(false);
  };

  const handleSave = () => {
    if (!hasSignature) return;
    const exportCanvas = document.createElement("canvas");
    const ctx = exportCanvas.getContext("2d");
    if (!ctx) return;
    exportCanvas.width = width;
    exportCanvas.height = height;
    ctx.clearRect(0, 0, width, height);
    ctx.strokeStyle = "#1e293b";
    ctx.lineWidth = 2;
    ctx.lineCap = "round";
    ctx.lineJoin = "round";
    paths.forEach(p => {
      if (p.length < 2) return;
      ctx.beginPath();
      ctx.moveTo(p[0].x, p[0].y);
      for (let i = 1; i < p.length; i++) ctx.lineTo(p[i].x, p[i].y);
      ctx.stroke();
    });
    onSave(exportCanvas.toDataURL("image/png"));
  };

  return (
    <div className="nbr-sigpad">
      <div className="nbr-sigpad-canvas-wrap">
        <canvas
          ref={canvasRef}
          onMouseDown={startDrawing}
          onMouseMove={draw}
          onMouseUp={stopDrawing}
          onMouseLeave={stopDrawing}
          onTouchStart={startDrawing}
          onTouchMove={draw}
          onTouchEnd={stopDrawing}
          style={{ touchAction: "none" }}
        />
        {!hasSignature && (
          <div className="nbr-sigpad-placeholder">Hier unterschreiben</div>
        )}
      </div>
      <div className="nbr-sigpad-actions">
        <button className="nbr-btn nbr-btn-sm" onClick={handleClear} disabled={!hasSignature}>
          <Trash2 size={11} /> Löschen
        </button>
        <button className="nbr-btn nbr-btn-sm" onClick={handleUndo} disabled={paths.length === 0}>
          <Undo2 size={11} /> Rückgängig
        </button>
        <div style={{ flex: 1 }} />
        <button className="nbr-btn nbr-btn-primary nbr-btn-sm" onClick={handleSave} disabled={!hasSignature}>
          <Check size={11} /> Übernehmen
        </button>
      </div>
    </div>
  );
}

