function sanitizeEmailHtml(html: string): string {
  // Script-Tags, Event-Handler und externe Ressourcen entfernen
  return html
    .replace(/<script[\s\S]*?<\/script>/gi, "")
    .replace(/on\w+="[^"]*"/gi, "")
    .replace(/on\w+='[^']*'/gi, "")
    .replace(/<style[\s\S]*?<\/style>/gi, "")
    .replace(/javascript:/gi, "");
}

