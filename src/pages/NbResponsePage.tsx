/**
 * NbResponsePage — NB-Antwort-Orchestrator UI mit Auto-Resolve
 * Zeigt offene NB-Rückfrage-Tasks, Complaints, Thread-Timeline,
 * Dokumenten-Checkliste, Anhang-Historie, Antwort-Vorschau
 */
import { useState, useEffect, useCallback, useRef } from "react";
import {
  Mail, FileText, Check, X, Clock, Send, RefreshCw,
  ChevronLeft, ChevronRight, AlertTriangle, CheckCircle2,
  Loader2, Bell, Eye, MessageSquare, Paperclip, ArrowDown,
  ArrowUp, Shield, Zap, HelpCircle, PenTool, Upload,
  Settings, Pen, Trash2, Undo2, Activity, Plus, XCircle,
  Search, MailCheck, FileWarning, UserCheck
} from "lucide-react";
import { apiGet, apiPost, apiPut } from "../api/client";
import { useAuth } from "../pages/AuthContext";
import "./nb-response.css";

// ═══════════════════════════════════════════════════════════════════════════════
// TYPES
// ═══════════════════════════════════════════════════════════════════════════════

interface NbComplaint {
  id: number;
  taskId: number;
  type: string;
  description: string;
  extractedDetail: any;
  resolved: boolean;
  resolveMethod: string | null;
  resolveDetail: string | null;
  resolvedAt: string | null;
  createdAt: string;
}

interface NbTask {
  id: number;
  installationId: number;
  triggerEmailId: number;
  status: string;
  requiredDocuments: DocRequirement[] | null;
  nbSummary: string | null;
  nbDeadline: string | null;
  vdeFormularSetId: number | null;
  vdeAutoGenerated: boolean;
  customerNotifiedAt: string | null;
  approvedAt: string | null;
  responseTo: string | null;
  responseSubject: string | null;
  responseBody: string | null;
  attachedDocumentIds: number[] | null;
  sentAt: string | null;
  errorMessage: string | null;
  resolveStatus: string | null;
  threadEmails: ThreadEmail[] | null;
  complaints: NbComplaint[];
  createdAt: string;
  updatedAt: string;
  installation: {
    id: number;
    publicId: string;
    customerName: string | null;
    gridOperator: string | null;
    status: string;
    netzbetreiberId: number | null;
  };
}

interface DocRequirement {
  name: string;
  type: string;
  present: boolean;
  source: "customer" | "system" | "vde";
}

interface TriggerEmail {
  id: number;
  subject: string | null;
  bodyText: string | null;
  bodyHtml: string | null;
  fromAddress: string | null;
  receivedAt: string;
  attachments: string | null;
}

interface ThreadEmail {
  id: number;
  direction: "INBOUND" | "OUTBOUND";
  fromAddress: string;
  toAddress?: string;
  subject: string | null;
  bodyText: string | null;
  date: string;
  source: string;
  attachments: Array<{
    id: number;
    fileName: string;
    mimeType: string;
    fileSize: number;
    documentType: string | null;
    isFromNb: boolean;
  }>;
  isTrigger?: boolean;
}

interface NbTaskComment {
  id: number;
  taskId: number;
  type: "SYSTEM" | "MANUAL";
  action: string;
  message: string;
  metadata: any;
  authorId: number | null;
  authorName: string | null;
  createdAt: string;
}

interface AttachmentRecord {
  id: number;
  fileName: string;
  mimeType: string;
  fileSize: number;
  documentType: string | null;
  isFromNb: boolean;
  createdAt: string;
}

interface TaskDetail {
  task: NbTask & {
    installation: NbTask["installation"] & {
      contactEmail: string | null;
      netzbetreiberId: number | null;
      documents: Array<{
        id: number;
        kategorie: string;
        originalName: string;
        dateiname: string;
        dateigroesse: number | null;
        createdAt: string;
      }>;
    };
  };
  triggerEmail: TriggerEmail | null;
  attachmentHistory: AttachmentRecord[];
  comments: NbTaskComment[];
}

const STATUS_LABELS: Record<string, { label: string; color: string }> = {
  ANALYZING: { label: "Analysiert...", color: "#EAD068" },
  COLLECTING: { label: "Sammelt Dokumente", color: "#F97316" },
  READY_FOR_REVIEW: { label: "Bereit", color: "#22C55E" },
  APPROVED: { label: "Freigegeben", color: "#3B82F6" },
  SENT: { label: "Gesendet", color: "#34d399" },
  CANCELLED: { label: "Abgebrochen", color: "#71717a" },
};

const RESOLVE_STATUS_LABELS: Record<string, { label: string; color: string }> = {
  resolved: { label: "Gelöst", color: "#22C55E" },
  partial: { label: "Teilweise", color: "#F97316" },
  manual_needed: { label: "Manuell", color: "#ef4444" },
  pending: { label: "Ausstehend", color: "#71717a" },
};

const COMPLAINT_TYPE_INFO: Record<string, { label: string; icon: any; color: string }> = {
  MISSING_SIGNATURE: { label: "Unterschrift fehlt", icon: PenTool, color: "#F97316" },
  SCHALTPLAN_ERROR: { label: "Schaltplan-Fehler", icon: Zap, color: "#ef4444" },
  WRONG_DATE: { label: "Falsches Datum", icon: Clock, color: "#F97316" },
  MISSING_DOCUMENT: { label: "Dokument fehlt", icon: FileText, color: "#ef4444" },
  FORM_INCOMPLETE: { label: "Formular unvollständig", icon: AlertTriangle, color: "#F97316" },
  CLARIFICATION_NEEDED: { label: "Klärung nötig", icon: HelpCircle, color: "#EAD068" },
};

// ═══════════════════════════════════════════════════════════════════════════════
// MAIN
// ═══════════════════════════════════════════════════════════════════════════════

export default function NbResponsePage() {
  const { user } = useAuth();
  const isAdmin = user?.role === "ADMIN";

  const [tasks, setTasks] = useState<NbTask[]>([]);
  const [total, setTotal] = useState(0);
  const [page, setPage] = useState(1);
  const [statusFilter, setStatusFilter] = useState<string>("");
  const [loading, setLoading] = useState(true);
  const [stats, setStats] = useState<Record<string, number>>({});
  const [selectedTaskId, setSelectedTaskId] = useState<number | null>(null);
  const [detail, setDetail] = useState<TaskDetail | null>(null);
  const [detailLoading, setDetailLoading] = useState(false);
  const [toast, setToast] = useState<{ msg: string; type: "success" | "error" } | null>(null);
  const [showSignatureModal, setShowSignatureModal] = useState(false);

  const loadTasks = useCallback(async () => {
    setLoading(true);
    try {
      const params: Record<string, string | number> = { page, limit: 20 };
      if (statusFilter) params.status = statusFilter;
      const data = await apiGet<{
        tasks: NbTask[];
        total: number;
        stats: Record<string, number>;
      }>("/api/nb-response/tasks?" + new URLSearchParams(params as Record<string, string>));
      setTasks(data.tasks);
      setTotal(data.total);
      setStats(data.stats);
    } catch {
      showToast("Fehler beim Laden", "error");
    }
    setLoading(false);
  }, [page, statusFilter]);

  useEffect(() => { loadTasks(); }, [loadTasks]);

  const loadDetail = async (taskId: number) => {
    setSelectedTaskId(taskId);
    setDetailLoading(true);
    try {
      const data = await apiGet<TaskDetail>(`/api/nb-response/tasks/${taskId}`);
      setDetail(data);
    } catch {
      setDetail(null);
    }
    setDetailLoading(false);
  };

  const showToast = (msg: string, type: "success" | "error" = "success") => {
    setToast({ msg, type });
    setTimeout(() => setToast(null), 3000);
  };

  const handleApprove = async () => {
    if (!detail || !isAdmin) return;
    const confirmed = window.confirm(
      `NB-Antwort wirklich senden?\n\nAn: ${detail.task.responseTo || "(kein Empfänger)"}\nBetreff: ${detail.task.responseSubject || "—"}\n\nDiese Aktion kann nicht rückgängig gemacht werden.`
    );
    if (!confirmed) return;
    try {
      await apiPost(`/api/nb-response/tasks/${detail.task.id}/approve`, {
        body: detail.task.responseBody,
        subject: detail.task.responseSubject,
      });
      showToast("NB-Antwort gesendet!");
      loadTasks();
      loadDetail(detail.task.id);
    } catch {
      showToast("Senden fehlgeschlagen", "error");
    }
  };

  const handleCancel = async () => {
    if (!detail || !isAdmin) return;
    if (!window.confirm("Task wirklich abbrechen?")) return;
    try {
      await apiPost(`/api/nb-response/tasks/${detail.task.id}/cancel`, {});
      showToast("Task abgebrochen");
      loadTasks();
      loadDetail(detail.task.id);
    } catch {
      showToast("Abbrechen fehlgeschlagen", "error");
    }
  };

  const handleRefresh = async () => {
    if (!detail) return;
    try {
      await apiPost(`/api/nb-response/tasks/${detail.task.id}/refresh`, {});
      showToast("Aktualisiert");
      loadDetail(detail.task.id);
    } catch {
      showToast("Fehler", "error");
    }
  };

  const handleReResolve = async () => {
    if (!detail || !isAdmin) return;
    try {
      await apiPost(`/api/nb-response/tasks/${detail.task.id}/re-resolve`, {});
      showToast("Re-Analyse gestartet");
      loadDetail(detail.task.id);
    } catch {
      showToast("Re-Analyse fehlgeschlagen", "error");
    }
  };

  const handleRequestCustomer = async () => {
    if (!detail || !isAdmin) return;
    try {
      await apiPost(`/api/nb-response/tasks/${detail.task.id}/request-customer`, {});
      showToast("Kunde benachrichtigt");
      loadDetail(detail.task.id);
    } catch {
      showToast("Fehler", "error");
    }
  };

  const handleResponseEdit = async (body: string) => {
    if (!detail) return;
    try {
      await apiPut(`/api/nb-response/tasks/${detail.task.id}/response`, { body });
      setDetail((prev) => prev ? { ...prev, task: { ...prev.task, responseBody: body } } : null);
    } catch {
      showToast("Speichern fehlgeschlagen", "error");
    }
  };

  const pages = Math.ceil(total / 20);

  return (
    <div className="nbr-container">
      {/* Header */}
      <div className="nbr-header">
        <h1><Mail size={22} /> NB-Antworten</h1>
        <div className="nbr-stats-badges">
          <StatBadge label="Offen" count={stats.COLLECTING || 0} color="#F97316" />
          <StatBadge label="Bereit" count={stats.READY_FOR_REVIEW || 0} color="#22C55E" />
          <StatBadge label="Gesendet" count={stats.SENT || 0} color="#34d399" />
          {isAdmin && (
            <button
              className="nbr-btn"
              style={{ marginLeft: 8 }}
              onClick={() => setShowSignatureModal(true)}
              title="EFK-Signaturen verwalten"
            >
              <Settings size={14} /> EFK-Signatur
            </button>
          )}
        </div>
      </div>

      {/* Filter */}
      <div className="nbr-toolbar">
        <div className="nbr-filter-chips">
          <button className={`nbr-chip ${!statusFilter ? "active" : ""}`} onClick={() => { setStatusFilter(""); setPage(1); }}>Alle</button>
          <button className={`nbr-chip ${statusFilter === "COLLECTING" ? "active" : ""}`} onClick={() => { setStatusFilter("COLLECTING"); setPage(1); }}>Sammelt</button>
          <button className={`nbr-chip ${statusFilter === "READY_FOR_REVIEW" ? "active" : ""}`} onClick={() => { setStatusFilter("READY_FOR_REVIEW"); setPage(1); }}>Bereit</button>
          <button className={`nbr-chip ${statusFilter === "SENT" ? "active" : ""}`} onClick={() => { setStatusFilter("SENT"); setPage(1); }}>Gesendet</button>
        </div>
        <button className="nbr-btn" onClick={loadTasks}><RefreshCw size={12} /> Aktualisieren</button>
      </div>

      {/* Content */}
      {loading ? (
        <div className="nbr-loading"><Loader2 size={16} className="nbr-spin" /> Laden...</div>
      ) : tasks.length === 0 ? (
        <div className="nbr-empty">Keine NB-Response-Tasks vorhanden</div>
      ) : (
        <div className="nbr-content">
          <div className="nbr-table-wrap">
            <table className="nbr-table">
              <thead>
                <tr>
                  <th>Installation</th>
                  <th>Netzbetreiber</th>
                  <th>Status</th>
                  <th>Resolve</th>
                  <th>Dokumente</th>
                  <th>Erstellt</th>
                  <th></th>
                </tr>
              </thead>
              <tbody>
                {tasks.map((t) => {
                  const docs = t.requiredDocuments || [];
                  const present = docs.filter((d) => d.present).length;
                  return (
                    <tr
                      key={t.id}
                      className={selectedTaskId === t.id ? "selected" : ""}
                      onClick={() => loadDetail(t.id)}
                    >
                      <td>
                        <div className="nbr-cell-name">{t.installation.customerName || t.installation.publicId}</div>
                        <div className="nbr-cell-sub">{t.installation.publicId}</div>
                      </td>
                      <td className="nbr-cell-sub">{t.installation.gridOperator || "—"}</td>
                      <td><StatusBadge status={t.status} /></td>
                      <td>{t.resolveStatus && <ResolveBadge status={t.resolveStatus} />}</td>
                      <td>
                        <span className={`nbr-doc-count ${present === docs.length && docs.length > 0 ? "complete" : ""}`}>
                          {present}/{docs.length}
                        </span>
                      </td>
                      <td className="nbr-cell-sub">{timeAgo(t.createdAt)}</td>
                      <td><Eye size={14} style={{ color: "#52525b" }} /></td>
                    </tr>
                  );
                })}
              </tbody>
            </table>
            {pages > 1 && (
              <div className="nbr-pagination">
                <span>{total} Tasks</span>
                <div className="nbr-page-btns">
                  <button disabled={page <= 1} onClick={() => setPage(page - 1)}><ChevronLeft size={14} /></button>
                  <span>{page} / {pages}</span>
                  <button disabled={page >= pages} onClick={() => setPage(page + 1)}><ChevronRight size={14} /></button>
                </div>
              </div>
            )}
          </div>

          {/* Detail Panel */}
          {(selectedTaskId || detailLoading) && (
            <DetailPanel
              detail={detail}
              loading={detailLoading}
              isAdmin={isAdmin}
              onClose={() => { setSelectedTaskId(null); setDetail(null); }}
              onApprove={handleApprove}
              onCancel={handleCancel}
              onRefresh={handleRefresh}
              onReResolve={handleReResolve}
              onRequestCustomer={handleRequestCustomer}
              onResponseEdit={handleResponseEdit}
              onReloadDetail={() => selectedTaskId && loadDetail(selectedTaskId)}
              showToast={showToast}
            />
          )}
        </div>
      )}

      {/* Signature Modal */}
      {showSignatureModal && (
        <SignatureModal onClose={() => setShowSignatureModal(false)} showToast={showToast} />
      )}

      {toast && <div className={`nbr-toast nbr-toast-${toast.type}`}>{toast.msg}</div>}
    </div>
  );
}

