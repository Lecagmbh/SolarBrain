// ═══════════════════════════════════════════════════════════════════════════════

function CustomerSignatureSection({
  vdeFormularSetId,
  showToast,
}: {
  vdeFormularSetId: number;
  showToast: (msg: string, type: "success" | "error") => void;
}) {
  const [showPad, setShowPad] = useState(false);
  const [saving, setSaving] = useState(false);
  const [signed, setSigned] = useState(false);

  const handleSignatureSave = async (base64: string) => {
    setSaving(true);
    try {
      await apiPost(`/api/vde/sets/${vdeFormularSetId}/sign`, {
        signatur: base64,
      });
      showToast("Kunden-Unterschrift gespeichert", "success");
      setSigned(true);
      setShowPad(false);
    } catch {
      showToast("Speichern fehlgeschlagen", "error");
    }
    setSaving(false);
  };

  if (signed) {
    return (
      <div className="nbr-customer-sig-done">
        <CheckCircle2 size={14} /> Kunden-Unterschrift gespeichert
      </div>
    );
  }

  return (
    <div className="nbr-customer-sig">
      {showPad ? (
        <div>
          <div style={{ fontSize: 12, color: "#a1a1aa", marginBottom: 6 }}>
            Kunden-Unterschrift für VDE-Formulare zeichnen:
          </div>
          <InlineSignaturePad
            onSave={handleSignatureSave}
          />
          {saving && (
            <div className="nbr-loading" style={{ padding: 8 }}>
              <Loader2 size={14} className="nbr-spin" /> Speichern...
            </div>
          )}
          <button className="nbr-btn nbr-btn-sm" style={{ marginTop: 4 }} onClick={() => setShowPad(false)}>
            Abbrechen
          </button>
        </div>
      ) : (
        <button className="nbr-btn nbr-btn-sm" onClick={() => setShowPad(true)}>
          <Pen size={12} /> Kunden-Unterschrift zeichnen
        </button>
      )}
    </div>
  );
}

